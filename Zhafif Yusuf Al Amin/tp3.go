package main

import "fmt"

func main() {
    // Deklarasi dengan var
    var nama string = "Zhafif"
    var umur int = 20

    // Deklarasi singkat dengan :=
    alamat := "Tanjungpinang"
    tinggi := 170.5

    fmt.Println("Nama:", nama)
    fmt.Println("Umur:", umur)
    fmt.Println("Alamat:", alamat)
    fmt.Println("Tinggi:", tinggi)
}
